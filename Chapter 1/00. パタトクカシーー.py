#2つの文字列「パトカー」と「タクシー」の文字を先頭から交互に連結し、
# 文字列「パタトクカシーー」を得よ。
# ======================================
# 00. 字符串交替合并
# ======================================
# 题目：
# 将“パトカー”和“タクシー”按位置交替拼接，得到“パタトクカシーー”
#
# 思路：
# 1. 用 zip() 按位置配对
# 2. 每一对拼接
# 3. 用 join() 合并成字符串

s1 = "パトカー"                        # 1つ目の文字列
s2 = "タクシー"                        # 2つ目の文字列
result_00 = "".join(s1[i] + s2[i] for i in range(len(s1)))  # 文字列を順番に連結する
print("00:", result_00)               # 結果を出す

# s1 = パ  ト  カ  ー
# s2 = タ  ク  シ  ー
#      ↓  ↓  ↓  ↓
# (パ,タ) (ト,ク) (カ,シ) (ー,ー)
#           ↓
# パタ / トク / カシ / ーー

# まず、rangeを使ってインデックスを取得します。

# 次に、s1[i] と s2[i] を使って、
# 同じ位置の文字を取り出します。

# それらを結合すると、
# パタ、トク、カシのような文字列になります。

# 最後に、join関数を使って、
# それらを1つの文字列にまとめます。

# その結果、パタトクカシーー が得られます。